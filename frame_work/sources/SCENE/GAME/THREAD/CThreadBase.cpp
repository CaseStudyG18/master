//*****************************************************************************
// コンストラクタ
//*****************************************************************************
CThreadBase::CThreadBase(LPDIRECT3DDEVICE9 *pDevice) : CScene2D(pDevice)
{
	// 描画しない
	SetDrawFlag(false);
	
	// 変数初期化
	m_nCount = 0;
}

//*****************************************************************************
// デストラクタ
//*****************************************************************************
CThreadBase ::~CThreadBase(void)
{
}

//*****************************************************************************
// 初期化
//*****************************************************************************
HRESULT CThreadBase::Init()
{

	return S_OK;
}

//*****************************************************************************
// 終了
//*****************************************************************************
void CThreadBase::Uninit(void)
{
	CScene2D::Uninit();
}

//*****************************************************************************
// 更新
//*****************************************************************************
void CThreadBase::Update(void)
{
	m_nCount++;

	// 寿命？
	if (m_nCount > m_nEndTime){
		Uninit();
	}

	// 当たり判定フラグ更新
	if (m_nCount > m_nHitStartTime){
		m_bHitFlg = true;
	}
	if (m_nHitEndTime < m_nCount){
		m_bHitFlg = false;
	}
	

}
