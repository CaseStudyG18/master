#include "CGoal.h"


//*****************************************************************************
// マクロ
//*****************************************************************************
const float GOAL_WIDTH = 80;
const float GOAL_HEIGHT = 80;
const TEXTURE_TYPE GOAL_TEXTURE = TEXTURE_GOAL;


//*****************************************************************************
// 静的メンバ変数
//*****************************************************************************

//*****************************************************************************
// コンストラクタ
//*****************************************************************************
CGoal::CGoal(LPDIRECT3DDEVICE9 *pDevice, int nPriority, OBJTYPE m_objType) : CScene2D(pDevice, nPriority, m_objType)
{
}

//*****************************************************************************
// デストラクタ
//*****************************************************************************
CGoal ::~CGoal(void)
{
}

//*****************************************************************************
// 初期化
//*****************************************************************************
void CGoal::Init()
{

}

//*****************************************************************************
// 終了
//*****************************************************************************
void CGoal::Uninit(void)
{
	CScene2D::Uninit();
}

//*****************************************************************************
// 更新
//*****************************************************************************
void CGoal::Update(void)
{
}

//*****************************************************************************
// クリエイト関数
//*****************************************************************************
CGoal* CGoal::Create(LPDIRECT3DDEVICE9 *pDevice, D3DXVECTOR3 pos, short nPlayerNum)
{
	// 作成
	CGoal* p = new CGoal(pDevice);

	// 初期化
	p->CScene2D::Init(pos, GOAL_WIDTH, GOAL_HEIGHT, GOAL_TEXTURE);

	p->m_nPlayerNum = nPlayerNum;
	p->m_bTranceFlg = false;

	return p;
}
