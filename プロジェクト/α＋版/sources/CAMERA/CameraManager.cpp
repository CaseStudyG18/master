#include "CameraManager.h"

//*****************************************************************************
// マクロ
//*****************************************************************************

//=================================================
// コンストラクタ
//=================================================
CCameraManager::CCameraManager(void)
{
	m_pCamera = new CCamera;
}

//=================================================
// デストラクタ
//=================================================
CCameraManager::~CCameraManager(void)
{
	m_pCamera = NULL;
}

//=================================================
// 初期化
// 引数: 座標、注視点座標
//=================================================
void CCameraManager::InitCamera(D3DXVECTOR3 pos, D3DXVECTOR3 posR)
{
	m_pCamera->Init(pos, posR);
}

//=================================================
// 終了
//=================================================
void CCameraManager::Uninit(void)
{
	if(m_pCamera)
	{
		m_pCamera->Uninit();
		delete m_pCamera;
		m_pCamera = NULL;
	}
}

//=================================================
// 更新
//=================================================
void CCameraManager::Update(void)
{
	m_pCamera->Update();
}


